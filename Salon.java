package segundoParcial;

		public class Salon {
		    private String nombre;
		    private int capacidad;

		    public Salon(String nombre, int capacidad) {
		        this.nombre = nombre;
		        this.capacidad = capacidad;
		    }

		    public String getNombre() {
		        return nombre;
		    }

		    public int getCapacidad() {
		        return capacidad;
		    }
		}

		
		

